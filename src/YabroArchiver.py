def run_tkinter1():  # первое гавно
    # импорты для ткинтера
    import tkinter as tk  # сам ткинтер
    from tkinter import filedialog  # диалоги
    from tkinter import messagebox  # сообщения
    import zipfile  # для зипа
    import tarfile  # для тара
    import rarfile  # для рара
    import py7zr  # для 7z
    
    # класс для окна
    class UglyTkinterWindow:  # уродское окно
        # конструктор
        def __init__(self):  # инициализация
            self.root = tk.Tk()  # создаем окно
            self.root.title("УРОДСКИЙ Tkinter РАСПАКОВЩИК")  # заголовок
            self.root.geometry("600x400")  # размер
            self.root.configure(bg='#ff0000')  # красный фон блять
            
            # рамка для кнопок
            btn_frame = tk.Frame(self.root, bg='#ff0000')  # рамка
            btn_frame.pack(side=tk.LEFT, fill=tk.Y)  # слева
            
            # кнопка выбора
            tk.Button(btn_frame, text="ВЫБРАТЬ АРХИВ", command=self.select_archive,
                     bg='#ff6666', fg='white', font=('Comic Sans MS', 12, 'bold')).pack(pady=5)
            
            # кнопка распаковки
            tk.Button(btn_frame, text="РАСПАКОВАТЬ", command=self.extract,
                     bg='#ff6666', fg='white', font=('Comic Sans MS', 12, 'bold')).pack(pady=5)
            
            # кнопка выхода
            tk.Button(btn_frame, text="ВЫХОД", command=self.root.quit,
                     bg='#ff6666', fg='white', font=('Comic Sans MS', 12, 'bold')).pack(pady=5)
            
            # надпись что нихуя не выбрано
            self.label = tk.Label(self.root, text="НИЧЕГО НЕ ВЫБРАНО", bg='#ff0000', 
                                 fg='white', font=('Arial', 16, 'bold'))
            self.label.pack(pady=50)  # размещаем
            
            self.archive_path = None  # путь к архиву пока пустой
        
        # функция выбора
        def select_archive(self):  # выбор архива
            self.archive_path = filedialog.askopenfilename(  # диалог
                title="Выбери архив, дебил",  # заголовок
                filetypes=[("Все архивы", "*.zip *.rar *.7z *.tar *.gz *.bz2"), ("Все файлы", "*.*")]
            )
            if self.archive_path:  # если выбрали
                self.label.config(text=f"АРХИВ: {os.path.basename(self.archive_path)}")  # меняем надпись
        
        # функция распаковки
        def extract(self):  # распаковка
            if not self.archive_path:  # если не выбрано
                messagebox.showerror("ОШИБКА", "СНАЧАЛА ВЫБЕРИ АРХИВ, ТУПОЙ!")  # ругаемся
                return  # выходим
            
            extract_dir = filedialog.askdirectory(title="КУДА РАСПАКОВЫВАТЬ?")  # выбираем папку
            if not extract_dir:  # если не выбрали
                return  # выходим
            
            try:  # пытаемся
                if self.archive_path.endswith('.zip'):  # если зип
                    with zipfile.ZipFile(self.archive_path, 'r') as z:  # открываем
                        z.extractall(extract_dir)  # распаковываем
                elif self.archive_path.endswith(('.tar', '.gz', '.bz2')):  # если тар
                    with tarfile.open(self.archive_path, 'r:*') as t:  # открываем
                        t.extractall(extract_dir)  # распаковываем
                elif self.archive_path.endswith('.rar'):  # если рар
                    with rarfile.RarFile(self.archive_path) as r:  # открываем
                        r.extractall(extract_dir)  # распаковываем
                elif self.archive_path.endswith('.7z'):  # если 7z
                    with py7zr.SevenZipFile(self.archive_path, mode='r') as z:  # открываем
                        z.extractall(extract_dir)  # распаковываем
                else:  # если хуйня
                    messagebox.showerror("ОШИБКА", "НЕИЗВЕСТНЫЙ ФОРМАТ, ИДИОТ!")  # ругаемся
                    return  # выходим
                
                messagebox.showinfo("УСПЕХ!", "АРХИВ РАСПАКОВАН! ЕЩЕ ХОЧЕШЬ?")  # радуемся
            except Exception as e:  # если ошибка
                messagebox.showerror("FAIL", f"НЕ ПОЛУЧИЛОСЬ: {str(e)}")  # говорим
        
        # запуск
        def run(self):  # основной цикл
            self.root.mainloop()  # запускаем
    
    # создаем окно
    window = UglyTkinterWindow()  # объект
    window.run()  # запускаем

